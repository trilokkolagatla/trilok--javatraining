package project;

import java.util.Scanner;

public class decimal
{
	public static void main(String [] args){
		int n;
		int d=0;
		System.out.print("enter the number n: ");
		Scanner scan = new Scanner(System.in);
		n = scan.nextInt();
		while (n!=0)
		
		{
			d=d*10;
			d= d+n%10;
			n=n/10;
		}
		System.out.print(d);
	}
	
}
	